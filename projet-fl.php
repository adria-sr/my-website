        <h2>
  Un écosystème graphique peut se définir comme "les outils et les règles graphiques qui garantissent la composition cohérente et ordonnée du nouveau paysage" mais il est aussi une "base ouverte qui se prête à être complétée et enrichie avec de nouvelles séries et développements graphiques." (Leku Studio). La notion d'écosystème induit aussi des interactions, des échanges et s'oppose au un kit graphique en s'inscrivant dans un contexte précis, un réalité tangible qui, dans ce cas, est la place Fontaine Lestang.<br><br>

  Le développement de l'écosystème se déroule en trois temps/ateliers. Un premier atelier in situ permet de faire connaitre le projet et de faire découvrir au plus grand nombre toutes les possibilités d'un tel dispositif au travers une sélection d'exemples d'installation graphiques déjà existantes. Un deuxième atelier en salle permet aux participants de faire de leurs propres mains en dessinant sur un fond de carte à partir de formes prédéfinies afin de mettre en valeur les espaces les plus délaissés de la place. Enfin, le troisième atelier s'organise comme une restitution de toutes les étapes du projet. Lors de ce dernier temps, trois propositions d'écosystème graphique sont soumises au vote. Elles sont réalisées par le designer graphique à l'issue du deuxième atelier et sont pensées comme une synthèse de toutes les participations. Enfin, en fonction des retours, un dernier retravail de la proposition sélectionnée est réalisé avant l'installation de l'écosystème graphique.<br><br>

  Au fur et à mesure de l'avancement du projet, les supports de communication se teintent de la participation des habitants : l'identité visuelle "rubalise", avec les formes de sa grille et sa tonique orange est déconstruite et réappropriée par les participants qui jouissent d'une liberté totale dans l'utilisation des formes proposées dans le deuxième atelier.
